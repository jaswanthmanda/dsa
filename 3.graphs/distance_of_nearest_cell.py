class Solution:
    def nearest(self, grid):
        m = len(grid)
        n = len(grid[0])

        newVal = copy.deepcopy(grid)
        q = deque()
        visited = set()

        for i in range(m):
            for j in range(n):
                if grid[i][j] == 1:
                    q.append((i, j, 0))

        while q:
            a, b, c = q.popleft()
            visited.add((a, b))
            newVal[a][b] = c

            vals = [
                (a - 1, b),
                (a + 1, b),
                (a, b - 1),
                (a, b + 1),
            ]

            for val in vals:
                aa, ab = val[0], val[1]
                if (
                    0 <= aa < m
                    and 0 <= ab < n
                    and grid[aa][ab] == 0
                    and (aa, ab) not in visited
                ):
                    visited.add((aa, ab))
                    q.append((aa, ab, c + 1))

        return newVal


